💡 常见问题
─────────────────────────────────────────────────────────────
  Q: 格子太大/太小？
  A: 调整 --cell-size 参数（默认64）

  Q: 窗口太大/太小？
  A: 调整 --render-width 和 --render-height

  Q: 训练太慢？
  A: 使用 ./train_no_viz.sh 或降低 --render-every

  Q: 想看得更清楚？
  A: 使用 ./train_slow.sh 或降低 --render-fps

